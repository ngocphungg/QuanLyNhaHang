package model;

/**
 *
 * @author admin
 */
public class tkmodel {
    String matk,tentk,pass,quyen;

    public tkmodel(String matk, String tentk, String pass, String quyen) {
        this.matk = matk;
        this.tentk = tentk;
        this.pass = pass;
        this.quyen = quyen;
    }

    public tkmodel() {
    }

    public String getMatk() {
        return matk;
    }

    public void setMatk(String matk) {
        this.matk = matk;
    }

    public String getTentk() {
        return tentk;
    }

    public void setTentk(String tentk) {
        this.tentk = tentk;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public String getQuyen() {
        return quyen;
    }

    public void setQuyen(String quyen) {
        this.quyen = quyen;
    }
    
}
